    private void code1FormattedTextFieldFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_code1FormattedTextFieldFocusLost
        try {
            if (!tree.existNode(Integer.parseInt(code1FormattedTextField.getText().replaceAll(" ", "").replaceAll("\t", "")))) {
                JOptionPane.showMessageDialog(this, "no existe el nodo, intente con un nuevo codigo", "Error", JOptionPane.ERROR_MESSAGE);
                saveButton.setEnabled(false);
            } else {
                saveButton.setEnabled(true);
            }
        } catch (errorException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_code1FormattedTextFieldFocusLost

