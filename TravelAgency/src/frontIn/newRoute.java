    private void saveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveButtonActionPerformed
        try {
            if (tree.existNode(Integer.parseInt(code2FormattedTextField.getText().replaceAll(" ", "").replaceAll("\t", ""))) && tree.existNode(Integer.parseInt(code2FormattedTextField.getText().replaceAll(" ", "").replaceAll("\t", "")))) {
                if (costoFormattedTextField.getText().replaceAll(" ", "").replaceAll("\t", "").isEmpty() && flightTimeFormattedTextField.getText().replaceAll(" ", "").replaceAll("\t", "").isEmpty()) {
                    JOptionPane.showMessageDialog(this, "Datos incompletos", "Error", JOptionPane.ERROR_MESSAGE);
                } else {
                    tree.addTR(code1FormattedTextField.getText(), code2FormattedTextField.getText(), costoFormattedTextField.getText(), flightTimeFormattedTextField.getText());
                    clear();
                    this.setVisible(false);
                    JOptionPane.showMessageDialog(this, "Destino agregado exitosamente", "Exito", JOptionPane.INFORMATION_MESSAGE);
                }
            } else {
                JOptionPane.showMessageDialog(this, "no existe el nodo, intente con un nuevo codigo", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } catch (errorException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_saveButtonActionPerformed

